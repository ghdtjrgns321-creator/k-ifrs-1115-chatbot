# 💼 [실전 테스트 1] 복합 수행의무 (Bundled Services)
# 가장 흔하지만 판단이 까다로운 '끼워팔기' 사례들입니다.

# 플랫폼/통신사 실무: "스마트폰 기기를 팔면서 2년 통신 약정을 같이 묶어서 팔았어요. 기기값은 대폭 할인해줬는데, 이때 통신 수익과 기기 매출을 어떻게 배분해서 인식해야 하나요?"

# 검증 포인트: 거래가격의 배분(개별 판매가격 비율)과 할인액 배분 규정을 정확히 가져오는지 확인.

# 설치 용역의 구별: "기계장치를 판매하면서 설치 용역을 무상으로 제공하기로 했습니다. 근데 이 설치가 너무 복잡해서 우리 회사 엔지니어만 할 수 있다면, 기계 인도 시점에 수익을 다 잡아도 되나요?"

# 검증 포인트: '구별되는 재화나 용역'의 요건(문단 27) 중 '다른 자원과 함께하여 효익을 얻을 수 있는지'를 파악하는지 확인.

# 📉 [실전 테스트 2] 변동대가와 환불부채 (Variable Consideration)
# 미래의 불확실성을 회계처리해야 하는 까다로운 영역입니다.

# 불확실성 제약: "신제품을 처음 출시하면서 대리점에 반품을 100% 받아주기로 했습니다. 시장 반응을 몰라서 과거 반품 데이터가 전혀 없는데, 출고 시점에 수익을 인식해도 될까요?"

# 검증 포인트: 변동대가 추정치의 제약(문단 56)에 따라 수익 인식을 보류해야 한다는 결론을 내는지 확인.

# 보너스와 페널티: "건설 계약인데, 조기 완공하면 보너스를 받고 지연되면 페널티를 물어야 해요. 완공일이 날씨 때문에 불확실한데 변동대가를 기댓값으로 추정해야 하나요, 아니면 가능성이 가장 높은 금액으로 해야 하나요?"

# 검증 포인트: 변동대가 추정 방법(기댓값 vs 가능성이 가장 높은 금액)의 선택 기준(결과치가 많은지 적은지)을 설명하는지 확인.

# 🏢 [실전 테스트 3] 본인 대 대리인 (Principal vs. Agent)
# 요즘 IT 플랫폼 기업들의 가장 큰 회계 이슈입니다.

# 배달/오픈마켓 플랫폼: "배달 앱 플랫폼입니다. 음식값과 배달비를 고객에게 한 번에 결제받고, 음식점과 배달기사에게 정산해줍니다. 우리는 플랫폼 수수료만 매출로 잡아야 하나요, 아니면 전체 결제액(그로스)을 매출로 잡아야 하나요?"

# 검증 포인트: 고객에게 재화나 용역이 이전되기 전에 플랫폼이 '통제'하는지(문단 B34)를 기준으로 본인/대리인을 판단하는지 확인.

# 신용위험과 통제권: "상품의 재고 위험은 우리가 안 지는데, 고객이 돈을 안 낼 위험(신용위험)은 우리가 전적으로 져요. 그럼 우리가 본인인가요?"

# 검증 포인트: 신용위험을 부담한다는 사실만으로는 본인(통제권 가짐)으로 볼 수 없다는 핵심 지침(문단 B37)을 찾아내는지 확인.

# 📜 [실전 테스트 4] 라이선스와 프랜차이즈 (Licenses & Franchise)
# 접근권(기간)이냐 사용권(시점)이냐를 가르는 실무 단골 질문입니다.

# 가맹비 수익인식: "프랜차이즈 본사인데, 가맹점 계약할 때 가맹비 5천만 원을 한 번에 받았습니다. 간판 달아주고 매뉴얼 교육해주는 의무가 있는데, 이 가맹비 수익은 언제 인식하나요?"

# 검증 포인트: 가맹비가 별도의 수행의무인지, 아니면 전체 프랜차이즈 권리(접근권)에 포함되어 기간에 걸쳐 인식해야 하는지 분석하는지 확인.

# 소프트웨어 업데이트: "보안 소프트웨어 라이선스를 3년간 부여했습니다. 보안 특성상 우리가 매일 최신 바이러스 패턴으로 업데이트를 해줘야 하는데, 이 라이선스는 한 시점에 수익을 잡나요, 기간에 걸쳐 잡나요?"

# 검증 포인트: 기업의 활동이 지적재산에 유의적인 영향을 미치므로 '접근권(기간 인식)'으로 분류(문단 B58)함을 찾아내는지 확인.

# 🔄 [실전 테스트 5] 실무 엣지 케이스 (Edge Cases)
# 현업에서 "이거 어떻게 처리하지?" 하고 멍해지는 순간들입니다.

# 계약변경의 늪: "100개 제품을 납품하는 계약을 진행 중인데, 50개 납품한 상태에서 단가를 깎아주고 50개를 추가로 납품하기로 계약을 바꿨습니다. 이거 기존 계약의 수정인가요, 새로운 계약인가요?"

# 검증 포인트: 추가되는 재화가 구별되지만 가격이 개별 판매가격만큼 상승하지 않았으므로, 기존 계약을 종료하고 새로운 계약을 체결한 것으로 회계처리(문단 21)함을 찾아내는지 확인.

# 비현금 대가의 변동: "고객이 돈 대신 자기네 회사 주식으로 결제한대요. 근데 계약 시점 주가랑 실제 주식 받을 때 주가가 다르면, 거래가격은 언제를 기준으로 측정해요?"

# 검증 포인트: 비현금 대가의 공정가치는 '계약 개시일'을 기준으로 측정하며, 그 이후의 공정가치 변동은 거래가격에 반영하지 않는다는 규정(문단 68)을 확인.

# 🗣️ [실전 테스트 6] 툭 던지는 구어체 (Advanced Slang)
# 비전문가가 대충 물어봤을 때의 대응력을 봅니다.

# 보증(AS): "물건 팔고 1년짜리 기본 AS 말고, 돈 더 내면 3년으로 연장해주는 거 팔았는데 이거 수익 매출이랑 같이 잡아도 됨?"

# 검증 포인트: 확신유형 보증(기본 AS)과 용역유형 보증(연장 AS)을 구분하여 별도 수행의무로 처리해야 함을 안내하는지 확인.

# 고객충성제도: "물건 팔면서 우리 백화점 상품권 얹어줬는데, 이거 어차피 공짜로 준 거니까 매출에서 안 까고 다 수익 잡아도 되지?"

# 검증 포인트: 고객에게 중요한 권리를 제공하는 것이므로 별도의 수행의무로 보아 거래가격을 배분(이연)해야 함을 짚어내는지 확인.