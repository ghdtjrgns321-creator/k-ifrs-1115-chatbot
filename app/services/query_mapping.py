QUERY_MAPPING: dict[str, list[str]] = {
    k: [term.strip() for term in v.split(",") if term.strip()]
    for k, v in _RAW_QUERY_MAPPING.items()
}


# 역매핑: 기준서 공식 용어 → 실무 용어 리스트
# generate.py에서 답변 시 실무 담당자 친화적 표현을 함께 제시하는 데 사용
INVERTED_MAPPING: dict[str, list[str]] = {}
for _practitioner_term, _official_terms in QUERY_MAPPING.items():
    for _term in _official_terms:
        INVERTED_MAPPING.setdefault(_term, []).append(_practitioner_term)


def _expand_query(query: str) -> str:
    """QUERY_MAPPING 딕셔너리로 실무 용어를 기준서 공식 용어로 확장합니다.

    매핑에 없는 쿼리는 원본 그대로 반환합니다.
    콤마로 연결된 문자열 대신 공백으로 엮인 문자열("A B C")을 반환하여
    BM25 토크나이저 및 임베딩 모델의 성능 저하(콤마 노이즈)를 방지합니다.
    """
    query_stripped = query.strip()
    expanded_list = QUERY_MAPPING.get(query_stripped, [query_stripped])
    return " ".join(expanded_list)
